package com.picoplaca.exception;

public class GetDigitException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public GetDigitException() {
		super();
		
	}

	public GetDigitException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public GetDigitException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public GetDigitException(String message) {
		super(message);
		
	}

	public GetDigitException(Throwable cause) {
		super(cause);
		
	}
	
	

}
